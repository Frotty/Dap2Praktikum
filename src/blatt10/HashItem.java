package blatt10;



class HashItem {
	private String key;
	private int info = 0;
	
	public HashItem (String a, int b) {
		key = a;
		info = b;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String a) {
		key = a;
	}
	public int getInfo() {
		return info;
	}
	public void setInfo(int b) {
		info = b;
	}
}
		
			
		 
